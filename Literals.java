package com.company;

public class Literals {
    public static void main(String[] args) {
        byte age1 = 10;
        int age2 = 50;
        char ch = 'A';
        boolean a = true;
        // in case of literals double, float, char 'd','f','l' should be specified resp.
        double d1 = 4.65D;
        float f1 = 5.6F;
        long ageDino = 10000000L;
        /* only variable can be printed
        example: age1,age2, ch */
        String s = "hello";
        // string literals
        System.out.println(age1);
        System.out.println(age2);
        System.out.println(ch);
        System.out.println(a);
        System.out.println(d1);
        System.out.println(f1);
        System.out.println(ageDino);
        System.out.println(s);

    }
}
